// ---------------------------------------------------------------------------
// Arg parsing
// ---------------------------------------------------------------------------
function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--name" && argv[i + 1]) args.name = argv[++i];
    if (argv[i] === "--mode" && argv[i + 1]) args.mode = argv[++i];
    if (argv[i] === "--path" && argv[i + 1]) args.targetPath = argv[++i];
    if (argv[i] === "--brief" && argv[i + 1]) args.brief = argv[++i];
    if (argv[i] === "--dry-run") args.dryRun = true;
  }
  return args;
}

// ---------------------------------------------------------------------------
// Readline helpers
// ---------------------------------------------------------------------------
function ask(rl, question) {
  return new Promise((resolve) =>
    rl.question(question, (ans) => resolve(ans.trim())),
  );
}

// ---------------------------------------------------------------------------
// File system helpers
// ---------------------------------------------------------------------------
function copyDirRecursive(src, dest, dryRun) {
  if (!fs.existsSync(src)) return;
  if (!dryRun) fs.mkdirSync(dest, { recursive: true });

  for (const item of fs.readdirSync(src)) {
    const srcPath = path.join(src, item);
    const destPath = path.join(dest, item);
    const stat = fs.statSync(srcPath);
    if (stat.isDirectory()) {
      copyDirRecursive(srcPath, destPath, dryRun);
    } else {
      if (dryRun) {
        info(`  [dry-run] copy ${srcPath} → ${destPath}`);
      } else {
        fs.copyFileSync(srcPath, destPath);
      }
    }
  }
}

function writeFile(filePath, content, dryRun) {
  if (dryRun) {
    info(`  [dry-run] write ${filePath}`);
    return;
  }
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, "utf8");
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
async function main() {
  const args = parseArgs(process.argv.slice(2));
  const dryRun = args.dryRun || false;

  hdr("Masonry — New Project Wizard");
  if (dryRun) warn("Dry-run mode — no files will be written\n");

  // Check template dir
  if (!fs.existsSync(TEMPLATE_DIR)) {
    err(`Template directory not found: ${TEMPLATE_DIR}`);
    err("Make sure you are running this from the Bricklayer2.0 repository.");
    process.exit(1);
  }

  // ---- Interactive prompts if flags missing --------------------------------
  let rl;
  const needInteractive = !args.name || !args.mode;
  if (needInteractive) {
    rl = readline.createInterface({
      input: process.stdin,
      output: process.stdout,
    });
  }

  // Project name
  let projectName = args.name;
  if (!projectName) {
    projectName = await ask(rl, `  ${c("cyan", "Project name")}: `);
  }
  if (!projectName) {
    err("Project name is required.");
    process.exit(1);
  }

  // Mode
  let mode = args.mode;
  if (!mode) {
    process.stdout.write(`\n  ${c("cyan", "Modes")}:\n`);
    for (const [key, val] of Object.entries(MODES)) {
      process.stdout.write(`    ${bold(key.padEnd(12))} ${dim(val.desc)}\n`);
    }
    process.stdout.write("\n");
    mode = await ask(rl, `  ${c("cyan", "Mode")}: `);
  }
  if (!MODES[mode]) {
    err(`Unknown mode: ${mode}. Valid modes: ${Object.keys(MODES).join(", ")}`);
    if (rl) rl.close();
    process.exit(1);
  }

  // Target directory
  let targetPath = args.targetPath;
  if (!targetPath) {
    const defaultPath = path.join(process.cwd(), projectName);
    const answer = await ask(
      rl,
      `  ${c("cyan", "Target directory")} ${dim(`[${defaultPath}]`)}: `,
    );
    targetPath = answer || defaultPath;
  }
  targetPath = path.resolve(targetPath);

  if (rl) rl.close();

  // ---- Summary before writing ---------------------------------------------
  process.stdout.write("\n");
  info(`Name:   ${bold(projectName)}`);
  info(`Mode:   ${bold(mode)} — ${dim(MODES[mode].desc)}`);
  info(`Path:   ${bold(targetPath)}`);
  info(`Brief:  ${args.brief ? bold(args.brief) : dim("none")}`);
  process.stdout.write("\n");

  if (fs.existsSync(targetPath) && !dryRun) {
    warn(`Directory already exists: ${targetPath}`);
    warn("Files will be overwritten.");
    process.stdout.write("\n");
  }

  // ---- Step 1: Copy template -----------------------------------------------
  hdr("Step 1 — Copying template");
  copyDirRecursive(TEMPLATE_DIR, targetPath, dryRun);
  ok("Template copied");

  // ---- Step 2: Write masonry.json ------------------------------------------
  hdr("Step 2 — Writing masonry.json");
  const masonryJson = {
    name: projectName,
    mode,
    domain: projectName,
    created_at: new Date().toISOString(),
  };
  writeFile(
    path.join(targetPath, "masonry.json"),
    JSON.stringify(masonryJson, null, 2),
    dryRun,
  );
  ok("masonry.json written");

  // ---- Step 3: Mode scaffold -----------------------------------------------
  hdr("Step 3 — Mode scaffold");
  const scaffold = MODES[mode].scaffold;

  if (scaffold === "evaluate") {
    const evalPath = path.join(targetPath, "evaluate.py");
    if (dryRun) {
      info(`[dry-run] would create ${evalPath}`);
    } else {
      writeFile(evalPath, EVALUATE_STUB, dryRun);
    }
    ok(`Created evaluate.py (${c("cyan", mode)} mode)`);
  } else if (scaffold === "simulate") {
    // simulate.py already comes from template
    const simPath = path.join(targetPath, "simulate.py");
    if (fs.existsSync(simPath)) {
      ok(`simulate.py present from template (${c("cyan", mode)} mode)`);
    } else {
      warn("simulate.py not found in template — add it manually");
    }
  } else {
    // fix mode — neither needed
    ok(`No scaffold needed for ${c("cyan", mode)} mode`);
  }

  // ---- Step 4: Copy brief if provided -------------------------------------
  if (args.brief) {
    hdr("Step 4 — Copying project brief");
    const briefSrc = path.resolve(args.brief);
    if (fs.existsSync(briefSrc)) {
      const briefDest = path.join(targetPath, "project-brief.md");
      if (!dryRun) fs.copyFileSync(briefSrc, briefDest);
      ok(`project-brief.md copied from ${briefSrc}`);
    } else {
      warn(`Brief file not found: ${briefSrc} — skipping`);
    }
  }

  // ---- Step 5: Generate registry -------------------------------------------
  hdr("Step 5 — Generating agent registry");
  try {
    if (dryRun) {
      info("[dry-run] would run generateRegistry()");
      ok("registry.json would be generated");
    } else {
      const registry = generateRegistry(targetPath);
      ok(`registry.json — ${registry.agents.length} agents indexed`);
    }
  } catch (e) {
    warn(`Registry generation failed: ${e.message}`);
  }

  // ---- Done ----------------------------------------------------------------
  hdr("Done");
  ok(`Project created at ${c("cyan", targetPath)}`);
  process.stdout.write("\n");
  process.stdout.write(`  ${c("purple", "Next steps")}:\n`);
  process.stdout.write(
    `    1. Edit ${bold("project-brief.md")} — describe the system and key invariants\n`,
  );
  if (scaffold === "evaluate") {
    process.stdout.write(
      `    2. Fill in placeholders in ${bold("evaluate.py")}\n`,
    );
  } else if (scaffold === "simulate") {
    process.stdout.write(
      `    2. Edit ${bold("simulate.py")} — replace stub with your actual model\n`,
    );
  }
  process.stdout.write(
    `    3. Run ${bold("/masonry-run")} to start a research campaign\n\n`,
  );
}

main().catch((e) => {
  process.stderr.write(`\nFatal: ${e.message}\n`);
  process.exit(1);
});
