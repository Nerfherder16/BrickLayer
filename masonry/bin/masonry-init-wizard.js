#!/usr/bin/env node
"use strict";
/**
 * bin/masonry-init-wizard.js — Interactive Masonry Project Creator
 *
 * Creates a new Masonry project from the template with mode-appropriate scaffolding.
 *
 * Usage (interactive):
 *   node bin/masonry-init-wizard.js
 *
 * Usage (flags):
 *   node bin/masonry-init-wizard.js --name <name> --mode <mode> --path <dir> [--brief <brief.md>] [--dry-run]
 *
 * Modes:
 *   benchmark, evolve, frontier, predict  → copies simulate.py (already in template)
 *   research, audit, diagnose, validate, monitor → creates evaluate.py stub
 *   fix → neither (template tests are sufficient)
 */

const fs = require("fs");
const path = require("path");
const os = require("os");
const readline = require("readline");
const { generateRegistry } = require("../src/core/registry");

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------
const MASONRY_ROOT = path.resolve(__dirname, "..");
const TEMPLATE_DIR = path.resolve(MASONRY_ROOT, "..", "template");

// ---------------------------------------------------------------------------
// ANSI helpers (same pattern as masonry-setup.js)
// ---------------------------------------------------------------------------
const CLR = {
  purple: "\x1b[38;2;139;92;246m",
  cyan: "\x1b[38;2;34;211;238m",
  green: "\x1b[38;2;52;211;153m",
  amber: "\x1b[38;2;245;158;11m",
  red: "\x1b[38;2;239;68;68m",
  dim: "\x1b[2m",
  bold: "\x1b[1m",
  reset: "\x1b[0m",
};
const c = (col, t) => `${CLR[col]}${t}${CLR.reset}`;
const dim = (t) => `${CLR.dim}${t}${CLR.reset}`;
const bold = (t) => `${CLR.bold}${t}${CLR.reset}`;

function log(msg) {
  process.stdout.write(`  ${msg}\n`);
}
function ok(msg) {
  log(`${c("green", "✓")} ${msg}`);
}
function warn(msg) {
  log(`${c("amber", "⚠")} ${msg}`);
}
function err(msg) {
  log(`${c("red", "✗")} ${msg}`);
}
function info(msg) {
  log(`${dim("·")} ${msg}`);
}
function hdr(msg) {
  process.stdout.write(`\n${c("purple", "🧱")}  ${bold(msg)}\n`);
}

// ---------------------------------------------------------------------------
// Mode definitions
// ---------------------------------------------------------------------------
const MODES = {
  benchmark: {
    desc: "Measure real system performance with instrumented test harnesses",
    scaffold: "simulate",
  },
  evolve: {
    desc: "Iterate on a model/system to optimize for a target metric",
    scaffold: "simulate",
  },
  frontier: {
    desc: "Probe capability limits and map the performance frontier",
    scaffold: "simulate",
  },
  predict: {
    desc: "Build and validate predictive models",
    scaffold: "simulate",
  },
  research: {
    desc: "Evidence-based investigation of an external topic or system",
    scaffold: "evaluate",
  },
  audit: {
    desc: "Structured compliance or quality audit",
    scaffold: "evaluate",
  },
  diagnose: {
    desc: "Root-cause analysis of an observed failure or anomaly",
    scaffold: "evaluate",
  },
  validate: {
    desc: "Verify that a system or claim meets defined criteria",
    scaffold: "evaluate",
  },
  monitor: {
    desc: "Track and alert on system health over time",
    scaffold: "evaluate",
  },
  fix: {
    desc: "Targeted bug-fix campaign using test-driven verification",
    scaffold: "none",
  },
};

const SIMULATE_MODES = new Set(["benchmark", "evolve", "frontier", "predict"]);
const EVALUATE_MODES = new Set([
  "research",
  "audit",
  "diagnose",
  "validate",
  "monitor",
]);

